// Operações aritméticas básicas com constantes "a" e "b". 
// Adição

const a = 12, b = 23;
resultado = a + b;
console.log("Adição = " + resultado);

// Subtração
resultado = a - b;
console.log("Subtração = " + resultado);

// Multiplicação
resultado = a * b;
console.log("Multiplicação = " + resultado);

// Divisão 
resultado = a / b;
console.log("Divisão = " + resultado);

// Módulo 

resultado = a % b;
console.log("Resto da divisão = " + resultado);
